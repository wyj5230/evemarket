package com.example.plugin.evemarketDTO;

public class SearchResult {

  String id;
  String group_id;
  String name;
  String description;
  String market_group_id;

  public String getId()
  {
    return id;
  }

  public void setId(String id)
  {
    this.id = id;
  }

  public String getGroup_id()
  {
    return group_id;
  }

  public void setGroup_id(String group_id)
  {
    this.group_id = group_id;
  }

  public String getName()
  {
    return name;
  }

  public void setName(String name)
  {
    this.name = name;
  }

  public String getDescription()
  {
    return description;
  }

  public void setDescription(String description)
  {
    this.description = description;
  }

  public String getMarket_group_id()
  {
    return market_group_id;
  }

  public void setMarket_group_id(String market_group_id)
  {
    this.market_group_id = market_group_id;
  }
}
